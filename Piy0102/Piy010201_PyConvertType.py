if __name__ == "__main__":

    """演示基本的类型转换"""

    # 字符串转整数
    var1 = "45"
    print("var1:", var1)
    print(type(var1))
    print(type(int(var1)))
    print()

    # 整数转字符串
    var2 = 912
    print("var2:", var1)
    print(type(var2))
    print(type(int(var2)))
